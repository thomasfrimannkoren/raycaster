/*Cosine table is shifted by 16 bits, use macros to divide and multiply
 * In our case 360 degrees is represented as 512 degrees.
 * To save on space, we store only 512/4 = 128 values as uint32_t. */
#define GET_COS_VAL(var, angle) \
	switch(angle >> ANGLE_QUADRANT_SHIFT){ \
		case (0x00): var = cos_table[angle & 0xFF]; break; \
		case (0x01): var = -cos_table[255 - (angle & 0xFF)]; break; \
		case (0x02): var = -cos_table[angle & 0xFF]; break; \
		case (0x03): var = cos_table[255 - (angle & 0xFF)]; break; \
		default: var = 0; break; \
	}

/*Cosine table is shifted by 16 bits, use macros to divide and multiply
 * In our case 360 degrees is represented as 512 degrees.
 * To save on space, we store only 512/4 = 128 values as uint32_t. */
#define GET_SIN_VAL(var, angle) \
	switch(angle >> ANGLE_QUADRANT_SHIFT){ \
		case (0x00): var = cos_table[255 - (angle & 0xFF)]; break; \
		case (0x01): var = cos_table[angle & 0xFF]; break; \
		case (0x02): var = -cos_table[255 - (angle & 0xFF)]; break; \
		case (0x03): var = -cos_table[angle & 0xFF]; break; \
		default: var = 0; break; \
	}

/*Tangent table is shifted by 16 bits, use macros to divide and multiply
 * In our case 360 degrees is represented as 512 degrees.
 * To save on space, we store only 512/4 = 128 values as uint32_t*/
#define GET_TAN_VAL(var, angle) \
	var = ( ( (angle >> ANGLE_QUADRANT_SHIFT) & 0x01 ) ? -tan_table[255 - (angle & 0xFF)] : tan_table[angle & 0xFF] )

#define TRIG_DIV(numerator, var) \
	( (numerator << 16) / var )

#define TRIG_MULT(multiplicand, var) \
	( (multiplicand * var) >> 16 )

#define ANGLE_ADD(angle, val) \
	( (angle + val) & 0x3FF )

#define ANGLE_SUB(angle, val) \
	( (angle + val) & 0x3FF )

